# ---------------------------------------------------------------------------
# Text without credit
# ---------------------------------------------------------------------------


class TestTextWithoutCredit:
    """Test that text without credit is returned unchanged."""

    def test_plain_text_unchanged(self):
        text = "Hello world"
        assert strip_whisper_credit(text) == "Hello world"

    def test_french_text_unchanged(self):
        text = "Bonjour le monde"
        assert strip_whisper_credit(text) == "Bonjour le monde"

    def test_empty_string_returned_as_is(self):
        assert strip_whisper_credit("") == ""

    def test_none_like_empty(self):
        # strip_whisper_credit expects a string; None should not be passed
        # but we test empty string as the closest valid input
        assert strip_whisper_credit("") == ""


# ---------------------------------------------------------------------------
# Credit with trailing whitespace
