class TestEnglishCreditStripping:
    """Test that English Whisper credit prefixes are correctly stripped."""

    def test_subtitles_generated_by_whisper(self):
        text = "Subtitles generated by Whisper Hello world"
        assert strip_whisper_credit(text) == "Hello world"

    def test_subtitles_generated_by_whisper_only(self):
        text = "Subtitles generated by Whisper"
