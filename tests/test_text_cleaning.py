    def test_credit_with_trailing_tab(self):
        text = "Sous-titres par Whisper\t"
        assert strip_whisper_credit(text) == ""

    def test_credit_with_trailing_newline(self):
        text = "Subtitles generated by Whisper\n"
        assert strip_whisper_credit(text) == ""


