# ---------------------------------------------------------------------------
# Edge cases
# ---------------------------------------------------------------------------


class TestEdgeCases:
    """Test edge cases for credit stripping."""

    def test_text_only_contains_credit(self):
        """Text that only contains a credit returns empty string."""
        text = "Sous-titres réalisées par Whisper"
        assert strip_whisper_credit(text) == ""

    def test_credit_at_start_with_content_after(self):
        text = "Sous-titres réalisées par Whisper This is the actual content"
        assert strip_whisper_credit(text) == "This is the actual content"

    def test_sous_titres_generes_by_whisper_only(self):
        text = "Sous-titres générés par Whisper"
        assert strip_whisper_credit(text) == ""

    def test_english_credit_only(self):
        text = "Subtitles generated by Whisper"
        assert strip_whisper_credit(text) == ""
